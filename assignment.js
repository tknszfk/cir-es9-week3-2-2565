let x = 20;

//1.Assignment and Addition
console.log(x += 10);//30
//2.Assignment and Subtraction
console.log(x -= 10);//20
//3.Assignment and Multiply
console.log(x *= 10);//200
//4.Assignment and Division
console.log(x /= 10);//20
//5.Assignment and Modulation 20/10
console.log(x %= 10);//0
//6.Assignment and Exponential
console.log(x **= 10);///0^10 =0